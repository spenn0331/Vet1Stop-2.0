export interface FilterOptions {
  category?: BusinessCategory;
  status?: VeteranStatus;
  stateCode?: string;
  keyword?: string;
  featuredOnly?: boolean;
}

export function filterBusinesses(opts: FilterOptions): Business[] {
  return BUSINESSES.filter(b => {
    if (opts.category && b.category !== opts.category) return false;
    if (opts.status && b.status !== opts.status) return false;
    if (opts.stateCode && b.stateCode !== opts.stateCode) return false;
    if (opts.featuredOnly && !b.featured) return false;
    if (opts.keyword) {
      const kw = opts.keyword.toLowerCase();
      const haystack = `${b.name} ${b.description} ${b.tags.join(' ')} ${b.city}`.toLowerCase();
      if (!haystack.includes(kw)) return false;
    }
    return true;
  });
}

