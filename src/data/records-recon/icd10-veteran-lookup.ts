/**
 * Look up an ICD-10 code. Tries exact match first, then falls back to
 * progressively shorter prefixes (e.g., F43.12 → F43.1 → F43 → F4).
 */
export function lookupICD10(code: string): ICD10Entry | undefined {
  const upper = code.toUpperCase();
  // Try exact match
  if (ICD10_LOOKUP[upper]) return ICD10_LOOKUP[upper];
  // Try without trailing digits (progressive fallback)
  const parts = upper.split('.');
  if (parts.length === 2) {
    // Try shortening decimal portion
    for (let len = parts[1].length - 1; len >= 0; len--) {
      const shorter = `${parts[0]}.${parts[1].substring(0, len)}`;
      if (ICD10_LOOKUP[shorter]) return ICD10_LOOKUP[shorter];
    }
    // Try just the base code
    if (ICD10_LOOKUP[parts[0]]) return ICD10_LOOKUP[parts[0]];
  }
  return undefined;
}
